
a = input("Enter value for A: ")
b = input("Enter value for B: ")
c = input("Enter value for C: ")

print("\n--- Before Swapping ---")
print("A =", a, "| B =", b, "| C =", c)

temp = a   
a = b      
b = c      
c = temp   

print("\n--- After Swapping ---")
print("A =", a, "| B =", b, "| C =", c)
